    .timeline-line.filling {
        /* Use a gradient to animate the orange fill */
        background: linear-gradient(to bottom, #f97316 0%, #d1d5db 0%);
        transition: background 0.3s ease;
    }

    .timeline-circle {
        width: 20px;
        height: 20px;
        background: white;
        border: 2px solid #fed7aa;
        border-radius: 9999px;
        transition: background 0.3s ease;
        top: calc(50%);
    }

    .timeline-circle.filled {
        background: #f97316;
        /* orange-500 */
    }
</style>

<script>
    document.addEventListener('DOMContentLoaded', () => {
        const timelineLine = document.querySelector('.timeline-line');
        const steps = document.querySelectorAll('.timeline-step');
        const circles = document.querySelectorAll('.timeline-circle');
        const container = timelineLine.parentElement;

        function updateLineFill() {
            const containerRect = container.getBoundingClientRect();
            const scrollTop = window.scrollY || window.pageYOffset;
            const viewportHeight = window.innerHeight;
            const containerTop = containerRect.top + scrollTop;
            const containerHeight = containerRect.height;

            // Calculate raw fill percentage (0% to 70% as in original)
            let fillPercentage = ((scrollTop + viewportHeight - containerTop) / containerHeight) * 70;
            fillPercentage = Math.max(0, Math.min(100, fillPercentage)); // Clamp between 0 and 100

            // Convert percentage to pixel height based on container height
            let fillHeightPx = (fillPercentage / 100) * containerHeight;

            // Enforce minimum height of 10px for the orange fill
            fillHeightPx = Math.max(10, fillHeightPx);

            // Calculate the percentage of the container height that the fillHeightPx represents
            const adjustedFillPercentage = (fillHeightPx / containerHeight) * 100;

            // Apply the gradient with adjusted fill percentage
            timelineLine.classList.add('filling');
            timelineLine.style.background = `linear-gradient(to bottom, #f97316 ${adjustedFillPercentage}%, #d1d5db ${adjustedFillPercentage}%)`;

            // Update circles based on raw fill percentage (unchanged)
            circles.forEach((circle, index) => {
                const step = steps[index];
                const stepRect = step.getBoundingClientRect();
                const stepTop = stepRect.top + scrollTop - containerTop;
                const circlePositionPercentage = (stepTop + 120) / containerHeight * 100;

                if (fillPercentage >= circlePositionPercentage) {
                    circle.classList.add('filled');
                } else {
                    circle.classList.remove('filled');
                }
            });
        }

        window.addEventListener('scroll', updateLineFill);
        updateLineFill(); // Initial call
    });
</script>