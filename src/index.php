<script type="text/javascript">
    const words = ["One Subscription", "On Demand", "Unlimited Leverage"];
    let currentWord = 0;
    let currentChar = 0;
    let isDeleting = false;
    const typingSpeed = 100;
    const deletingSpeed = 90;
    const pauseBetweenWords = 1000;
    const typedText = document.getElementById("typed-text");

    function type() {
        const word = words[currentWord];

        if (isDeleting) {
            typedText.textContent = word.substring(0, currentChar--);
        } else {
            typedText.textContent = word.substring(0, currentChar++);
        }

        if (!isDeleting && currentChar === word.length + 1) {
            isDeleting = true;
            setTimeout(type, pauseBetweenWords);
        } else if (isDeleting && currentChar === 0) {
            isDeleting = false;
            currentWord = (currentWord + 1) % words.length;
            setTimeout(type, 300);
        } else {
            setTimeout(type, isDeleting ? deletingSpeed : typingSpeed);
        }
    }

    document.addEventListener("DOMContentLoaded", () => {
        type();
    });
</script>


